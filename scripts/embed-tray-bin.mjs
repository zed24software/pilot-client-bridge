import { readFileSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const binPath = join(
  __dirname,
  "../node_modules/.pnpm/systray2@2.1.4/node_modules/systray2/traybin/tray_windows_release.exe"
);
const b64 = readFileSync(binPath).toString("base64");
const outPath = join(__dirname, "../src/traybin-embedded.ts");
writeFileSync(
  outPath,
  `// AUTO-GENERATED by scripts/embed-tray-bin.mjs — do not edit\nexport const trayBinBase64 = "${b64}";\n`
);
console.log(`Embedded tray binary (${(b64.length / 1024).toFixed(0)} KB base64)`);
