import { readFileSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const traybinDir = join(
  __dirname,
  "../node_modules/.pnpm/systray2@2.1.4/node_modules/systray2/traybin"
);

const winB64 = readFileSync(join(traybinDir, "tray_windows_release.exe")).toString("base64");
const darwinB64 = readFileSync(join(traybinDir, "tray_darwin_release")).toString("base64");

const outPath = join(__dirname, "../src/traybin-embedded.ts");
writeFileSync(
  outPath,
  `// AUTO-GENERATED by scripts/embed-tray-bin.mjs — do not edit\n` +
  `export const trayBinWinBase64 = "${winB64}";\n` +
  `export const trayBinDarwinBase64 = "${darwinB64}";\n`
);
console.log(`Embedded tray binaries (win: ${(winB64.length / 1024).toFixed(0)} KB, darwin: ${(darwinB64.length / 1024).toFixed(0)} KB base64)`);
